		<h3>Features</h3>
		<p>Privacy focused, Follw doesn't store any credentials other than your Sharing ID, location and configuration
		parameters.</p>
		<p>Add free, the Follw bussiness model is not based on advertisments and is free for personal and low volume
		use.</p>
		<p>Share any location, it doesn't have to be where you currently are.</p>
		<p>Control who can follow you when, prospone or retract following permission anytime and limit the follow
		duration.</p>
		<p>Documented API, easy integration with mapping software, tracking hardware and your custom software.</p>
		<p>Open Source, you can validate the security and workings of the software, copy it, modify it to your own
