		<h1>Follw</h1>
		<h2>Sharing your location with privacy</h2>
		<h3>Introduction</h3>
		<p>Follw is a privacy focused location sharing service. Only a unique Sharing ID and derived Sharing URL is
		given and no account details, user credentials, IP addresses, Cookies and other sensitive information are used
		or stored on the Follw servers.</p>
		<p>Whenever a new location is submitted the previous location is overwritten, no location history is
		stored.</p>
		<p>If you decide to delete your unique Sharing ID all location details are removed from the Follw.app servers.
		Only a hash of your Sharing ID is stored after removal to guarantee a Sharing ID is not reassigned again,
		everything else is removed from the Follw servers.</p>
